import mongoose from "mongoose"

const cardSchema = new mongoose.Schema({
    id: {
        type: String,
        required: true,
        unique: true,
    },
    title: {
        type: String,
        required: true,
        trim: true
    },
    description: {
        type: String,
        required: true,
        trim: true
    }
});

const Cardmodel = mongoose.model('Card', cardSchema);

export default Cardmodel